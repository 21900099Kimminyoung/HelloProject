package org.example;

public class Word {
    private String name;
    private String meaning;


    public String getName() {
        return name;
    }

    public String getMeaning() {
        return meaning;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setMeaning(String meaning) {
        this.meaning = meaning;
    }
}
