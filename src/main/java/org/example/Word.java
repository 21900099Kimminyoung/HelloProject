package org.example;

public class Word {
    private string name;
    private string meaning;


    public string getName() {
        return name;
    }

    public string getMeaning() {
        return meaning;
    }

    public void setName(string name) {
        this.name = name;
    }

    public void setMeaning(string meaning) {
        this.meaning = meaning;
    }
}
